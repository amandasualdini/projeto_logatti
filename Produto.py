class Produto:

    def get_id(self):
        return self.id
    
    def set_id(self, id):
        self.id = id

    def get_descricao(self):
        return self.descricao
    
    def set_descricao(self, descricao):
        self.descricao = descricao

    def get_valor(self):
        return self.valor
    
    def set_valor(self, valor):
        self.valor = valor
    
    def get_qtde_produtos(self):
        return self.qtde_produtos

    def set_qtde_produtos(self, qtde_produtos):
        self.qtde_produtos = qtde_produtos