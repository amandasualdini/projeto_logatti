class Funcionario:

    def get_id(self):
        return self.id
    
    def set_id(self, id):
        self.id = id

    def get_nome(self):
        return self.nome
    
    def set_nome(self, nome):
        self.nome = nome

    def get_cpf(self):
        return self.cpf
    
    def set_cpf(self, cpf):
        self.cpf = cpf
    
    def get_salario(self):
        return self.salario

    def set_salario(self, salario):
        self.salario = salario